package model;

public class Soldier {
    
 
    private String equipo;

    /**
     * @param equipo
     */
    public Soldier(String equipo) {
        this.equipo = equipo;
    }

    /**
     * @return the equipo
     */
    public String getEquipo() {
        return equipo;
    }

    /**
     * @param equipo the equipo to set
     */
    public void setEquipo(String equipo) {
        this.equipo = equipo;
    }
   
}
